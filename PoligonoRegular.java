public class PoligonoRegular {
    private int numLados;

    // Construtor que recebe o número de lados do polígono
    public PoligonoRegular(int numLados) {
        this.numLados = numLados;
    }

    // Método getter para obter o número de lados do polígono
    public int getNumLados() {
        return numLados;
    }

    // Método setter para definir o número de lados do polígono
    public void setNumLados(int numLados) {
        this.numLados = numLados;
    }
}